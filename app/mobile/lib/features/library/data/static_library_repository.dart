  static List<LibraryItem> getByCategory(LibraryCategory category) {
    return items.where((item) => item.category == category).toList();
  }

  static LibraryItem? getById(String id) {
    try {
      return items.firstWhere((item) => item.id == id);
    } catch (_) {
      return null;
    }
  }
